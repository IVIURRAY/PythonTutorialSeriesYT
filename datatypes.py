# int
x = 10
print(type(x))

# floats
y = 3.14
print(type(y))

# booleans
z = True
print(type(z))

# strings
name = 'haydn'
print(type(name))

# list
numbers = [1, 2, 3]
names = ['haydn', 'software', 'engineer']
print(type(names))

# tuples
coordinates = (10, 20)
print(type(coordinates))

# dictionary
person = {
    'name': 'haydn',
    'age': 21,
    'city': 'London'
}
print(person['name'])
print(person['age'])
print(person['city'])
print(type(person))

# set
# fruits = set(['apple', 'banana', 'banana'])
fruits = {'apple', 'banana', 'banana'}
print(fruits)
print(type(fruits))

# None
value = None
print(type(value))
